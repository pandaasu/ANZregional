/* View creation
/*-*/
create or replace force view mfj_dbp_rtn_ptd_view
   (SAP_SALES_ORG_CODE, SAP_DIVISION_CODE, SAP_DISTBN_CHNL_CODE, MFJ_DIV_CODE, MFJ_BUG_DESC, 
 MFJ_DIV_DESC, SORT_ORDER, GSV, BPS, NIV)
AS 
SELECT
T1.SAP_SALES_ORG_CODE,
T1.SAP_DIVISION_CODE,
T1.SAP_DISTBN_CHNL_CODE,
T1.MFJ_DIV_CODE,
T1.MFJ_BUG_DESC,
T1.MFJ_DIV_DESC,
T1.SORT_ORDER,
CASE
   WHEN T1.GSV <  1000000 THEN ROUND(T1.GSV/1000000,1)
   WHEN T1.GSV >= 1000000 THEN ROUND(T1.GSV/1000000)
   ELSE NULL
END "GSV",
CASE
   WHEN T1.BPS <  1000000 THEN ROUND(T1.BPS/1000000,1)
   WHEN T1.BPS >= 1000000 THEN ROUND(T1.BPS/1000000)
   ELSE NULL
END "BPS",
CASE
   WHEN T1.NIV <  1000000 THEN ROUND(T1.NIV/1000000,1)
   WHEN T1.NIV >= 1000000 THEN ROUND(T1.NIV/1000000)
   ELSE NULL
END "NIV"
FROM 
(
SELECT
  DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE     AS SAP_SALES_ORG_CODE,
  DD.DIVISION_DIM.SAP_DIVISION_CODE       AS SAP_DIVISION_CODE,
  DD.DISTBN_CHNL_DIM.SAP_DISTBN_CHNL_CODE AS SAP_DISTBN_CHNL_CODE,
  (DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE||DD.DIVISION_DIM.SAP_DIVISION_CODE||DD.DISTBN_CHNL_DIM.SAP_DISTBN_CHNL_CODE) AS MFJ_DIV_CODE,
  DECODE((DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE||DD.DIVISION_DIM.SAP_DIVISION_CODE||DD.DISTBN_CHNL_DIM.SAP_DISTBN_CHNL_CODE),
          '1315111', 'Petcare',
          '1315120', 'Petcare',
          '1315110', 'Food',
          '1315710', 'Food' ) AS MFJ_BUG_DESC,
  DECODE((DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE||DD.DIVISION_DIM.SAP_DIVISION_CODE||DD.DISTBN_CHNL_DIM.SAP_DISTBN_CHNL_CODE),
          '1315111', 'Pet Grocery',
          '1315120', 'Pet Specialist',
          '1315110', 'Snackfood',
          '1315710', 'Food' ) AS MFJ_DIV_DESC,
  DECODE((DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE||DD.DIVISION_DIM.SAP_DIVISION_CODE||DD.DISTBN_CHNL_DIM.SAP_DISTBN_CHNL_CODE),
          '1315111', 1,
          '1315120', 2,
          '1315110', 3,
          '1315710', 4 ) AS SORT_ORDER,
  sum(DD.SALES_PERIOD_01_FACT.SALES_DTL_PRICE_VALUE_13) AS GSV,
  sum(DD.SALES_PERIOD_01_FACT.SALES_DTL_PRICE_VALUE_2)  AS BPS,
  sum(DD.SALES_PERIOD_01_FACT.SALES_DTL_PRICE_VALUE_11) AS NIV
FROM
  DD.SALES_ORG_DIM,
  DD.DIVISION_DIM,
  DD.DISTBN_CHNL_DIM,
  DD.SALES_PERIOD_01_FACT,
  DD.MARS_DATE_PERIOD_DIM,
  DD.MATERIAL_DIM
WHERE
  ( DD.MARS_DATE_PERIOD_DIM.MARS_PERIOD=DD.SALES_PERIOD_01_FACT.BILLING_YYYYPP  )
  AND  ( DD.DISTBN_CHNL_DIM.DISTBN_CHNL_CODE=DD.SALES_PERIOD_01_FACT.SALES_DTL_DISTBN_CHNL_CODE  )
  AND  ( DD.DIVISION_DIM.DIVISION_CODE=DD.SALES_PERIOD_01_FACT.SALES_DTL_DIVISION_CODE  )
  AND  ( DD.SALES_ORG_DIM.SALES_ORG_CODE=DD.SALES_PERIOD_01_FACT.SALES_DTL_SALES_ORG_CODE  )
  AND  ( DD.MARS_DATE_PERIOD_DIM.MARS_PERIOD  =  ANY (SELECT
      TRUNC((max(DD.SALES_FACT.BILLING_YYYYPPDD)/100))
      FROM
      DD.SALES_FACT )
      AND  DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE  =  '131' )
  AND  ( DD.SALES_PERIOD_01_FACT.MATERIAL_CODE=DD.MATERIAL_DIM.MATERIAL_CODE )
  AND  ( DD.MATERIAL_DIM.SAP_MATERIAL_CODE  IN  
        ('10006530', '10006531', '10006532', '10006534', '10006535',
         'ET13313' , 'ET13314' , 'ET13315' , 'ET15014' , 'ET15015' , 'ET18101',
         'ET18102' , 'ET18104' , 'ET18105' , 'ET18107' , 'ET18108' , 'ET18110',
         'ET18111' , 'ET18113' , 'ET18114' , 'ET18116' , 'ET18117' , 'ET18125', 
         'ET18126' , 'ET18127' , 'ET18128' , 'ET18129') )
/*--- ¿¿¿¿¿¿¿¿¿¿¿¿¿¿¿¿¿ ---*/
  AND  ( DD.MATERIAL_DIM.SAP_MATERIAL_CODE  NOT IN  
         ('10018782','10018757','10018759','10018760','10018761',
          '10018762','10018763','10018764','10018765','10018766',
          '10018768','10018769','10018770','JP25554') )
/*
  AND  ( DD.MATERIAL_DIM.SAP_MATERIAL_CODE  IN  
        ('10006530', '10006531', '10006532', '10006534', '10006535',
         'ET13313' , 'ET13314' , 'ET13315' , 'ET15014' , 'ET15015' , 'ET18101',
         'ET18102' , 'ET18104' , 'ET18105' , 'ET18107' , 'ET18108' , 'ET18110',
         'ET18111' , 'ET18113' , 'ET18114' , 'ET18116' , 'ET18117' , 'ET18125', 
         'ET18126' , 'ET18127' , 'ET18128' , 'ET18129') ) */
GROUP BY
  DD.SALES_ORG_DIM.SAP_SALES_ORG_CODE, 
  DD.DIVISION_DIM.SAP_DIVISION_CODE, 
  DD.DISTBN_CHNL_DIM.SAP_DISTBN_CHNL_CODE
ORDER BY
  SORT_ORDER
) T1;

